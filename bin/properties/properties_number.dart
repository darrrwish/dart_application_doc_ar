// isOdd: هل الرقم فردي؟
// isNegative: هل الرقم سالب؟
// isInfinite: هل الرقم لا نهائي؟
// isNaN: هل الرقم ليس رقمًا (Not-a-Number)?
// isFinite: هل الرقم محدود؟
